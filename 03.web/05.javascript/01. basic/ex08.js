// 동치 연산자

// Equal operator
// 자동 형변환
console.log('3.14E2' == 314)
console.log('0x10' == 16)
console.log('1' == 1)
console.log('1' != 1)

// Strict Equal operator
// 타입부터 확인해서 다르면 false, 같으면 값 비교
console.log('3.14E2' === 314)
console.log('0x10' === 16)
console.log('1' === 1)
console.log('1' !== 1)

console.log('9' + 2) // 이건 문자열
console.log('9' - 2)
console.log('9' * 2)
console.log('9' / 2)
